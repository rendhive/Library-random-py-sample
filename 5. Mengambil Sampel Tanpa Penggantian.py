import random

my_list = ['a', 'b', 'c', 'd', 'e']
sample = random.sample(my_list, 3)  # Mengambil 3 elemen acak dari list tanpa penggantian
print("Sampel dari list:", sample)
# Fungsi: Mengambil elemen acak dari kumpulan tanpa mengganti elemen yang diambil.
# Kondisi: Ketika Anda ingin memilih item secara acak dari kumpulan item.
