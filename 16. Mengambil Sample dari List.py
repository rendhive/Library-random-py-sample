import random

colors = ['red', 'green', 'blue', 'yellow', 'purple']
sample_color = random.choice(colors)  # Mengambil satu elemen acak dari list
print("Warna yang dipilih secara acak:", sample_color)
# Fungsi: Memilih elemen acak dari list.
# Kondisi: Ketika Anda ingin secara acak memilih item tertentu dari pilihan.
