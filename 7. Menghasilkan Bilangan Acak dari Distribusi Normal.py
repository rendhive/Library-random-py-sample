import random

mean = 0
stddev = 1
normal_random = random.gauss(mean, stddev)  # Menghasilkan bilangan acak dari distribusi normal
print("Bilangan acak dari distribusi normal:", normal_random)
# Fungsi: Menghasilkan angka acak dari distribusi normal dengan rata-rata dan deviasi standar tertentu.
